    private void preencherCamposEdicao(int codigoCliente) {
        ClientesDao cli = new ClientesDao();
        try {
            this.cliente = cli.getCliente(codigoCliente);
            this.nomeC.setText(this.cliente.getNomeC());
            this.enderecoC.setText(this.cliente.getEnderecoC());
            this.bairroC.setText(this.cliente.getBairroC());
            this.cepC.setText("" + this.cliente.getCepC());
            this.complementoC.setText(this.cliente.getComplementoC());
            this.celC.setText("" + this.cliente.getCelC());
        } catch (SQLException ex) {
            Logger.getLogger(ListagemCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void popularTabela() throws SQLException {
        ClientesDao cl = new ClientesDao();
        List<Clientes> listaCliente = cl.listarClientes();
        DefaultTableModel model = (DefaultTableModel) tableClient.getModel();
        List<Object> lista = new ArrayList<Object>();

        for (int i = 0; i < listaCliente.size(); i++) {
            Clientes c = listaCliente.get(i);
            lista.add(new Object[]{c.getCodigo(), c.getNomeC(), c.getEnderecoC(), c.getBairroC(), c.getComplementoC(), c.getCepC(), c.getCelC()});
        }
        for (int idx = 0; idx < lista.size(); idx++) {
            model.addRow((Object[]) lista.get(idx));
        }
    }

