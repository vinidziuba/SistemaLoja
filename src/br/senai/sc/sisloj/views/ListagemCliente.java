    private void botaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoActionPerformed
        Clientes cli = new Clientes();
        cli.setNomeC(nomeC.getText());
        cli.setEnderecoC(enderecoC.getText());
        cli.setBairroC(bairroC.getText());
        cli.setComplementoC(complementoC.getText());

        String cep = cepC.getText();
        cep = cep.replaceAll("[^0-9]", "");

        String celular = celC.getText();
        celular = celular.replaceAll("[^0-9]", "");

        cli.setCepC(Long.parseLong(cep));
        cli.setCelC(Long.parseLong(celular));
        cli.setCodigo(this.cliente.getCodigo());

        ClientesDao cliDao = new ClientesDao();

        try {
            cliDao.alterar(cli);
            JOptionPane.showMessageDialog(null, "Cadastro atualizado com sucesso !");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Falha ao atualizar cadastro !");
            Logger.getLogger(CadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_botaoActionPerformed

