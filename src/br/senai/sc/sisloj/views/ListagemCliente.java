    private void eliminaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminaActionPerformed
        // TODO add your handling code here:
        Object[] options = {"Sim", "Não"};
        int opcaoSelecionada = JOptionPane.showOptionDialog(null, "Deseja realmente eliminar este cliente ?", "Atenção!", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);

        if (opcaoSelecionada == 0) {

            ClientesDao cliDao = new ClientesDao();

            try {
                cliDao.eliminar(this.cliente.getCodigo());
                this.limparTabela();
                this.popularTabela();
                this.cl.show(this, "painelListagem");
            } catch (SQLException ex) {
                Logger.getLogger(ListagemCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_eliminaActionPerformed
    private void limparTabela() {
        ((DefaultTableModel) tableClient.getModel()).setNumRows(0);
        tableClient.updateUI();
    }

